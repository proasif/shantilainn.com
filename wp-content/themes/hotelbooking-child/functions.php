// 1. RUN AFTER PARENT THEME SETUP
function init_child_setup() {
	dequeu_scripts();
}

function dequeu_scripts() {
	wp_dequeue_style('nicdark-style');
	wp_deregister_style('nicdark-style');
}

// 2. DISABLE FRONT END ADMIN BAR
add_filter('show_admin_bar', '__return_false');
// --- 2 ---

// 3. ENQUEUE THE SCRIPTS
function enqueue_scripts() {
	
	// Font Awesome
	wp_enqueue_style( 'font-awesome', '//cdnjs.cloudflare.com/ajax/libs/font-awesome/4.6.1/css/font-awesome.min.css', array(), '4.6.1' );

	$parent_style = "parent-style";
	wp_enqueue_style( $parent_style, get_template_directory_uri() . '/style.css' );
    wp_enqueue_style( 'child-style',
        get_stylesheet_directory_uri() . '/style.css',
        array( $parent_style ),
        wp_get_theme()->get('Version')
    );
}
add_action( 'wp_enqueue_scripts', 'enqueue_scripts', 1);
